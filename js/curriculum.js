function openModal(dayId, e) {
  if (e) e.stopPropagation();
  const d = dayData[dayId];
  if (!d) return;
  document.getElementById('modalEyebrow').textContent = d.week;
  document.getElementById('modalTitle').textContent = d.title;

  const diffMap = { easy:['diff-easy','🟢 입문'], normal:['diff-normal','🟡 보통'], hard:['diff-hard','🔴 심화'] };
  const [diffCls, diffLabel] = (diffMap[d.diff] || diffMap.normal);

  const modal = document.getElementById('detailModal');
  const old = modal.querySelector('.modal-meta-bar');
  if (old) old.remove();
  modal.querySelector('.modal-header').insertAdjacentHTML('afterend',
    `<div class="modal-meta-bar"><span class="diff-badge ${diffCls}">${diffLabel}</span>${d.time ? `<span class="time-badge">⏱ ${d.time}</span>` : ''}</div>`);

  let html = '';
  if (d.why) html += `<div class="why-box"><div class="why-label">💬 왜 배우나요?</div><div class="why-text">${applyTooltips(d.why)}</div></div>`;
  if (d.tags) html += `<div class="content-block"><div class="tag-wrap">${d.tags.map(t=>`<span class="tag">${t}</span>`).join('')}</div></div>`;
  if (d.table) {
    html += `<div class="content-block"><div class="block-title">📋 핵심 내용</div><div class="info-card" style="padding:0;overflow:hidden"><table class="styled-table"><thead><tr>${d.tableHeaders.map(h=>`<th>${h}</th>`).join('')}</tr></thead><tbody>${d.table.map(row=>`<tr>${row.map(c=>`<td>${c}</td>`).join('')}</tr>`).join('')}</tbody></table></div>`;
    if (d.example) html += `<div class="example-box"><div class="example-label">📎 실무 예시</div><div class="example-text">${d.example}</div></div>`;
    html += '</div>';
  }
  if (d.items) {
    html += `<div class="content-block"><div class="block-title">📌 학습 내용</div><ul class="content-list">${d.items.map(i=>`<li>${i}</li>`).join('')}</ul>`;
    if (d.example && !d.table) html += `<div class="example-box"><div class="example-label">📎 실무 예시</div><div class="example-text">${d.example}</div></div>`;
    html += '</div>';
  }
  if (d.tips) html += `<div class="tip-box"><div class="tip-label">🐣 초보자 팁</div><ul class="tip-list">${d.tips.map(t=>`<li>${applyTooltips(t)}</li>`).join('')}</ul></div>`;
  if (d.mission) html += `<div class="mission-box" style="margin-top:20px"><div class="mission-label">🎯 오늘의 미션</div><div class="mission-text">${d.mission}</div></div>`;

  document.getElementById('modalBody').innerHTML = html;
  _modalLastFocus = document.activeElement;
  document.getElementById('detailOverlay').classList.add('open');
  document.body.style.overflow = 'hidden'; // 배경 스크롤 잠금
  const closeBtn = document.querySelector('#detailModal .modal-close');
  if (closeBtn) setTimeout(() => closeBtn.focus(), 30); // 모달로 포커스 이동
}

let _modalLastFocus = null;
function closeModal() {
  document.getElementById('detailOverlay').classList.remove('open');
  document.body.style.overflow = ''; // 스크롤 잠금 해제
  if (_modalLastFocus && _modalLastFocus.focus) { try { _modalLastFocus.focus(); } catch (_) {} _modalLastFocus = null; }
}
function closeModalOutside(e) { if (e.target === document.getElementById('detailOverlay')) closeModal(); }
document.addEventListener('keydown', e => { if (e.key === 'Escape') closeModal(); });

