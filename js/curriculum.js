// ─── Progress ───
function updateProgress() {
  const totalDays = (typeof dayData === 'object') ? Object.keys(dayData).length : 60;
  const validDays = new Set(Object.keys(dayData || {}));
  const done = [...getDoneSet()].filter(d => validDays.has(d)).length;
  const pct = totalDays ? Math.round((done / totalDays) * 100) : 0;
  const pctEl = document.getElementById('progressPct');
  const barEl = document.getElementById('progressBar');
  if (pctEl) pctEl.textContent = pct + '%';
  if (barEl) barEl.style.width = pct + '%';
  const doneEl = document.getElementById('progressDone');
  if (doneEl) doneEl.textContent = done;
}

function getDoneSet() {
  try { return new Set(JSON.parse(localStorage.getItem('pm_done') || '[]')); }
  catch (_) { return new Set(); }
}
function saveDoneSet(set) {
  try { localStorage.setItem('pm_done', JSON.stringify([...set])); } catch (_) {}
}
function toggleDone(el, e) {
  e.stopPropagation();
  const item = el.closest('.day-item');
  const day = item.getAttribute('data-day');
  const isDone = item.classList.toggle('done');
  el.textContent = isDone ? '✓' : '';
  el.setAttribute('aria-pressed', isDone ? 'true' : 'false');
  const set = getDoneSet();
  if (isDone) set.add(day); else set.delete(day);
  saveDoneSet(set);
  updateProgress();
}
// Re-apply saved completion state to all rendered day items
function restoreProgress() {
  const set = getDoneSet();
  document.querySelectorAll('.day-item').forEach(item => {
    const day = item.getAttribute('data-day');
    const chk = item.querySelector('.day-check');
    const done = set.has(day);
    item.classList.toggle('done', done);
    if (chk) { chk.textContent = done ? '✓' : ''; chk.setAttribute('aria-pressed', done ? 'true' : 'false'); }
  });
  updateProgress();
}

